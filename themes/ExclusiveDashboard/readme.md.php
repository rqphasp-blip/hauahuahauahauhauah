# Exclusive Dashboard

- **Theme Name:** Exclusive Dashboard
- **Theme Version:** 1.1.0
- **Theme Date:** 18/06/2024
- **Theme Author:** OpenAI Assistant
- **Theme License:** MIT

Exclusive Dashboard has been reworked to mirror the Hope UI Laravel dashboard foundation. The layout now adopts a split card s
tructure with a dark analytic sidebar, luminous gradients, and pill controls that echo the reference workspace.

## Features

- Hope UI-inspired scaffold with a spotlighted profile rail and white content canvas
- Gradient driven share action, link buttons, and icon containers with dashboard depth
- Ambient grid background and accent light blocks for a high-tech analytics feel
- Responsive adjustments that collapse into a single column while preserving hierarchy

Place the folder in your LinkStack `themes/` directory and activate it from the admin panel.